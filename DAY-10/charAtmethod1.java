class charAtmethod1
{

	public static void main(String []args)
	{
		String name = "James Smith";
		char ch1 = name.charAt(3);
		char ch2 = name.charAt(6);
		char ch3 = name.charAt(9);
		System.out.println("Our String name "+name);
		System.out.println("character at index 3-"+ch1);
		System.out.println("character at index 6-"+ch2);
		System.out.println("character at index 9-"+ch3);
		
	}
	
}